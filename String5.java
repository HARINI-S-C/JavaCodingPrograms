/******************************************************************************

Java Program for Calculating ASCII value of a character

Input:
l 

Output:
108

*******************************************************************************/
import java.util.*;
public class Main
{
	public static void main(String[] args) {
		Scanner s=new Scanner(System.in);
		char ch=s.next().charAt(0);
		int n=ch;
		System.out.println(n);
	}
}
